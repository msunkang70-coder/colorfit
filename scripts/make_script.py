"""발표 스크립트 Word 파일 생성 — 14장 구조, 자연스러운 구어체."""

from docx import Document
from docx.shared import Pt, Inches, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH

doc = Document()
style = doc.styles["Normal"]
style.font.name = "맑은 고딕"
style.font.size = Pt(11)

def hdr(num, title, t):
    p = doc.add_paragraph()
    r = p.add_run(f"슬라이드 {num}")
    r.bold = True; r.font.size = Pt(14); r.font.color.rgb = RGBColor(0x96,0x4F,0x4C)
    r = p.add_run(f"  {title}")
    r.bold = True; r.font.size = Pt(14)
    r = p.add_run(f"  ({t})")
    r.font.size = Pt(10); r.font.color.rgb = RGBColor(0x99,0x99,0x99)

def sec(label):
    p = doc.add_paragraph()
    r = p.add_run(f"[{label}]")
    r.bold = True; r.font.size = Pt(10); r.font.color.rgb = RGBColor(0x66,0x66,0x66)

def say(text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Inches(0.3)
    p.paragraph_format.space_after = Pt(3)
    r = p.add_run(text)
    r.font.size = Pt(11)

def tip(text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Inches(0.3)
    r = p.add_run(text)
    r.font.size = Pt(9); r.font.color.rgb = RGBColor(0x99,0x99,0x99); r.italic = True

def sep():
    doc.add_paragraph("─" * 50)

# 표지
p = doc.add_paragraph()
